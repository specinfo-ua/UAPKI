int dstu4145_params_get_ec(const AlgorithmIdentifier_t* aid, EcCtx **ec_ctx)
{
    int ret = RET_OK;
    char* curve_oid = NULL;

    long asn_f;
    int f_len;
    int f[5] = {0};

    long m;
    long a = 0;

    ByteArray *b = NULL;
    ByteArray *n = NULL;
    ByteArray *Pc = NULL;

    bool is_le, is_onb;

    DSTU4145Params_t* dstu_params = NULL;
    const DSTUEllipticCurve_t* ellipticCurve;

    EcCtx *curve = NULL;

    CHECK_PARAM(aid != NULL);
    CHECK_PARAM(ec_ctx != NULL);

    if (aid->parameters == NULL) {
        SET_ERROR(RET_INVALID_EC_PARAMS);
    }

    CHECK_NOT_NULL(dstu_params = asn_any2type(aid->parameters, get_DSTU4145Params_desc()));
    ellipticCurve = &dstu_params->ellipticCurve;

    if (ellipticCurve->present == DSTUEllipticCurve_PR_NOTHING) {
        SET_ERROR(RET_INVALID_EC_PARAMS);
    }
    
    DO(dstu4145_params_get_std_oid(ellipticCurve, &curve_oid));
    if (curve_oid != NULL) {
        EcParamsId ec_id;
        ec_id = ecid_from_oid(curve_oid);
        if (ec_id == EC_PARAMS_ID_UNDEFINED) {
            SET_ERROR(RET_UNSUPPORTED);
        }
        CHECK_NOT_NULL(curve = ec_alloc_default(ec_id));
    }
    else {
        if (ellipticCurve->choice.ecbinary.f.member->present == member_PR_NOTHING) {
            SET_ERROR(RET_INVALID_EC_PARAMS);
        }

        a = ellipticCurve->choice.ecbinary.a;
        DO(asn_OCTSTRING2ba(&ellipticCurve->choice.ecbinary.b, &b));
        DO(asn_INTEGER2ba(&ellipticCurve->choice.ecbinary.n, &n));
        DO(ba_swap(n)); //ec_alloc in le

        DO(asn_OCTSTRING2ba(&ellipticCurve->choice.ecbinary.bp, &Pc));

        DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.m, &m));

        if (ellipticCurve->choice.ecbinary.f.member->present == member_PR_trinomial) {
            f_len = 3;
            DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.m, &asn_f));
            f[0] = (int)asn_f;
            DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.member->choice.trinomial, &asn_f));
            f[1] = (int)asn_f;
        }
        else if (ellipticCurve->choice.ecbinary.f.member->present == member_PR_pentanomial) {
            f_len = 5;
            DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.m, &asn_f));
            f[0] = (int)asn_f;
            DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.member->choice.pentanomial.l, &asn_f));
            f[1] = (int)asn_f;
            DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.member->choice.pentanomial.j, &asn_f));
            f[2] = (int)asn_f;
            DO(asn_INTEGER2long(&ellipticCurve->choice.ecbinary.f.member->choice.pentanomial.k, &asn_f));
            f[3] = (int)asn_f;
        }
        else {
            SET_ERROR(RET_UNSUPPORTED);
        }

        DO(dstu_params_type(&aid->algorithm, &is_le, &is_onb));

        if (!is_le) {
            DO(ba_swap(b));
            DO(ba_swap(Pc));
        }

        if (is_onb) {
            CHECK_NOT_NULL(curve = ec_alloc_binary_onb((int)m, (int)a, b, n, Pc, NULL));
        }
        else {
            CHECK_NOT_NULL(curve = ec_alloc_binary_pb(f, f_len, (int)a, b, n, Pc, NULL));
        }
    }

    *ec_ctx = curve;
    curve = NULL;

cleanup:

    free(curve_oid);
    ec_free(curve);

    ba_free(b);
    ba_free(n);
    ba_free(Pc);

    asn_free(get_DSTU4145Params_desc(), dstu_params);

    return ret;
}
