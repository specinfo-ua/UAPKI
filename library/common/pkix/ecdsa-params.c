uint32_t ecdsa_ecparams_get_ecid (
        const uint8_t* buf,
        const size_t len
)
{
    uint32_t rv_ecid = EC_PARAMS_ID_UNDEFINED;

    if (!buf) return rv_ecid;

    //  by OID
    if ((len == sizeof(NIST_P256_OID)) && (memcmp(NIST_P256_OID, buf, sizeof(NIST_P256_OID)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P256;
    }
    else if ((len == sizeof(NIST_P384_OID)) && (memcmp(NIST_P384_OID, buf, sizeof(NIST_P384_OID)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P384;
    }
    else if ((len == sizeof(NIST_P521_OID)) && (memcmp(NIST_P521_OID, buf, sizeof(NIST_P521_OID)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P521;
    }
    //  by ECParameters
    else if ((len == sizeof(NIST_P256)) && (memcmp(NIST_P256, buf, sizeof(NIST_P256)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P256;
    }
    else if ((len == sizeof(NIST_P384)) && (memcmp(NIST_P384, buf, sizeof(NIST_P384)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P384;
    }
    else if ((len == sizeof(NIST_P521)) && (memcmp(NIST_P521, buf, sizeof(NIST_P521)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P521;
    }
    //  by ECParameters (without SEED)
    else if ((len == sizeof(NIST_P256_SEEDLESS)) && (memcmp(NIST_P256_SEEDLESS, buf, sizeof(NIST_P256_SEEDLESS)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P256;
    }
    else if ((len == sizeof(NIST_P384_SEEDLESS)) && (memcmp(NIST_P384_SEEDLESS, buf, sizeof(NIST_P384_SEEDLESS)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P384;
    }
    else if ((len == sizeof(NIST_P521_SEEDLESS)) && (memcmp(NIST_P521_SEEDLESS, buf, sizeof(NIST_P521_SEEDLESS)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P521;
    }
    //  by ECParameters OpenSSL
    else if ((len == sizeof(OPENSSL_P521)) && (memcmp(OPENSSL_P521, buf, sizeof(OPENSSL_P521)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P521;
    }
    else if ((len == sizeof(OPENSSL_P521_SEEDLESS)) && (memcmp(OPENSSL_P521_SEEDLESS, buf, sizeof(OPENSSL_P521_SEEDLESS)) == 0)) {
        rv_ecid = EC_PARAMS_ID_NIST_P521;
    }

    return rv_ecid;
}
